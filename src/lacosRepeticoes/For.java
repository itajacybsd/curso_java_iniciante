package lacosRepeticoes;

public class For {
    static void main() {
        for (int i = 0; i <=5; i++) {
            System.out.println(i);
        }
            System.out.println("---------------------");

        for (int i = 1; i <= 10; i++) {
            System.out.println(i);
        }
            System.out.println("---------------------");

        int[] numeros = {10,20,30,40};
        for (int i = 0; i < numeros.length; i++) {
            System.out.println(numeros[i]);

        }
            System.out.println("---------------------");
    }
}
